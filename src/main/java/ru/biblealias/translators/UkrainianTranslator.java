    public Command getBotsCommand(String message) {
        if(message.toLowerCase(Locale.ROOT).contains("–ø—Ä–∏–≤i—Ç")) {
            return Command.HELLO;
        }
        else if (message.toLowerCase(Locale.ROOT).contains("–≤—ñ–¥–≥—É–∫:")) {
            return Command.FEEDBACK_MESSAGE;
        }
        else {
            return switch (message) {
                case "–¢–∞–∫", "—Ç–∞–∫" -> Command.YES;
                case "–ù—ñ", "–Ω—ñ" -> Command.NO;
                case "–ì—Ä–∞—Ç–∏ –∑ –ø–æ—Ç–æ—á–Ω–∏–º–∏ –Ω–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è–º–∏" -> Command.PLAY_USING_THE_CURRENT_SETTINGS;
                case "–ü–æ—á–∞—Ç–∏ –Ω–æ–≤—É –≥—Ä—É" -> Command.START_NEW_GAME;
                case "–ó–∞–∫—ñ–Ω—á–∏—Ç–∏ –≥—Ä—É" -> Command.STOP_GAME;
                case "–ù–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è" -> Command.SETTINGS;
                case "–ó–∞–ª–∏—à–∏—Ç–∏ –≤—ñ–¥–≥—É–∫" -> Command.INSTRUCTION;
                case "–Ü–Ω—Å—Ç—Ä—É–∫—Ü—ñ—è" -> Command.SEND_FEEDBACK;
                case "–ó–º—ñ–Ω–∏—Ç–∏ –Ω–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è" -> Command.CHANGE_SETTINGS;
                case "–ú–æ–≤–∞" -> Command.LANGUAGE;
                case "–†—É—Å—Å–∫–∏–π —è–∑—ã–∫" -> Command.RUSSIAN;
                case "English language" -> Command.ENGLISH;
                case "–£–∫—Ä–∞—ó–Ω—Å—å–∫–∞ –º–æ–≤–∞" -> Command.UKRAINIAN;
                case "–†–µ–∂–∏–º –≥—Ä–∏" -> Command.GAME_MODE;
                case "–°—Ç–∞—Ä–∏–π –ó–∞–ø–æ–≤—ñ—Ç \uD83D\uDD4E" -> Command.OLD_TESTAMENT;
                case "–ù–æ–≤–∏–π –ó–∞–ø–æ–≤—ñ—Ç ‚úùÔ∏èÔ∏è" -> Command.NEW_TESTAMENT;
                case "–£—Å—è –ë—ñ–±–ª—ñ—è \uD83D\uDCD6" -> Command.ALL_BIBLE;
                case "–†—ñ–∑–¥–≤—è–Ω–∏–π alias ‚ú®" -> Command.CHRISTMAS;
                case "–¢—Ä–∏–≤–∞–ª—ñ—Å—Ç—å —Ä–∞—É–Ω–¥–∞" -> Command.ROUND_DURATION;
                case "–®—Ç—Ä–∞—Ñ –∑–∞ –Ω–µ–≤—ñ–¥–≥–∞–¥–∞–Ω–µ —Å–ª–æ–≤–æ" -> Command.PENALTY;
                case "1 –±–∞–ª" -> Command.PENALTY_ONE;
                case "2 –±–∞–ª–∞" -> Command.PENALTY_TWO;
                case "–ë–µ–∑ —à—Ç—Ä–∞—Ñ—É" -> Command.PENALTY_ZERO;
                case "–û—Ç—Ä–∏–º–∞—Ç–∏ –∑–∞–≤–¥–∞–Ω–Ω—è \uD83D\uDCAC" -> Command.GET_TASK;
                case "–†–æ–∑–ø–æ—á–∞—Ç–∏ –≤—ñ–¥–≥–∞–¥—É–≤–∞—Ç–∏ —Å–ª–æ–≤–∞" -> Command.START_GUESSING_WORDS;
                case "–í—ñ–¥–≥–∞–¥–∞–ª–∏ ‚úîÔ∏è" -> Command.GUESSED;
                case "–ù–µ –≤—ñ–¥–≥–∞–¥–∞–ª–∏ ‚ùå" -> Command.NOT_GUESSED;
                default -> Command.DONT_UNDERSTAND;
            };
        }
    }

